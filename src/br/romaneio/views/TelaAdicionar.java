    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        // TODO add your handling code here:
        
        Dia dia = new Dia();
        ContadorDias cd = new ContadorDias();
        
        dia.setLitros(Integer.parseInt(campLitros.getText()));
        dia.setValorLeite(Double.valueOf(campoPreco.getText()));
        dia.setFurural(Double.valueOf(campoFurural.getText()));
        int mes = cd.calendario();
        dia.setMes(mes);
        
        DiaDAO dao = new DiaDAO();
        dao.inserir(dia);
        
        
        dao = null;
        dia= null;
        campLitros.setText("");
          
        
    }//GEN-LAST:event_salvarActionPerformed

